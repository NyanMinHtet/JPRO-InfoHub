 public void loadAllDataintoTable(ArrayList<Student>list){
        
        DefaultTableModel dtm=(DefaultTableModel) jTableItStd.getModel();
        dtm.setRowCount(0);
        
        for(Student std :list){
            Vector v=new Vector();
	    v.add(std.getStd_id());
            v.add(std.getStd_no());
            v.add(std.getName());
            v.add(std.getDob());
            v.add(std.getEmail());
	    v.add(std.getPhno());
            

            dtm.addRow(v);
            
        } 
      }
        public void loadtable(){
            
            StudentDAO stdao=new StudentDAO();
            ArrayList list=stdao.loaddataStd();
            loadAllDataintoTable(list);
        }
        
         public void loadAllDataintoTableRS(ArrayList<Relationship>list){
        
        DefaultTableModel dtm=(DefaultTableModel) jTableRsV.getModel();
        dtm.setRowCount(0);
        
        for(Relationship rs :list){
            Vector v=new Vector();
            
            v.add(rs.getName());
            v.add(rs.getPhno());
            v.add(rs.getOccupation());
	    
            v.add(rs.getRs_type());

            dtm.addRow(v);
            
        } 
      }
        public void loadtableRs(){
            
            RelationshipDAO rsdao=new RelationshipDAO();
            ArrayList list=rsdao.loaddataRS();
            loadAllDataintoTable(list);
        }
        
         public void loadAllDataintoTableQua(ArrayList<Qualification>list){
        
        DefaultTableModel dtm=(DefaultTableModel) jTableEduV.getModel();
        dtm.setRowCount(0);
        
        for(Qualification qua :list){
            Vector v=new Vector();
            v.add(qua.getDip_name());
            v.add(qua.getField_name());
            v.add(qua.getType());
	    v.add(qua.getOrg_name());
	    v.add(qua.getFrom_date());
	    v.add(qua.getTo_date());
            

            dtm.addRow(v);
            
        } 
      } 
        public void loadtableQua(){
            
            QualificationDAO quadao=new QualificationDAO();
            ArrayList list=quadao.loaddataQua();
            loadAllDataintoTable(list);
        }
        
     
        
        
