    private void btnUpdateStdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateStdActionPerformed
            
            index = jTableItStd.getSelectedRow();

            if(index<0){
               JOptionPane.showMessageDialog(null, "Please select any record from table for update");
            }else
            {       
                   ITstdUpdate up=new ITstdUpdate();
                    up.setVisible(true);
                    up.txtstd_id.setVisible(false);
                    up.txtimgpath.setVisible(false);
                 row_id=(int)jTableItStd.getValueAt(index, 0);
                
                StudentDAO  sdao=new StudentDAO();
                Student  std=sdao.returnAllDataToTextfieldStd(row_id); 
                up.txtstd_id.setText(String.valueOf(std.getStd_id()));
                up.txtstdIdU.setText(std.getStd_no());
                up.txtstdNameU.setText(std.getName());
                up.txtdateu.setDate(std.getDob());
                up.txtstdNRCU.setText(std.getNrc());
                up.txtstdEmailU.setText(std.getEmail());
                up.txtstdTeleU.setText(std.getTele_no());
                up.txtstdPhnoU.setText(std.getPhno());
                String gender = std.getGender();
                  if (gender.equals("Male")) {
                    up.rdoMaleU.setSelected(true);
                   } else if (gender.equals("Female")) {
                    up.rdoFemaleU.setSelected(true);
                          }
                   String religion = std.getReligion();
                   if(religion.equals("Buddish")){
                   up.rdoBuddishU.setSelected(true);
                   }else if(religion.equals("Christian")){
                   up.rdoChristianU.setSelected(true);
                   }else if(religion.equals("Hindu")){
                   up.rdoHinduU.setSelected(true);
                   }else if(religion.equals("Islam")){
                   up.rdoIslumU.setSelected(true);
                       }

                     String nation=std.getNation();
                     if(nation.equals("Myanmar")){
                     up.rdoMyanmarU.setSelected(true);
                     }else if(nation.equals("Others")){
                     up.rdoOtherU.setSelected(true);
            }
                String filePath = std.getPhoto();
                up.txtimgpath.setText(filePath);
                ImageIcon imageIcon = new ImageIcon(filePath);
                  up.lblupdateimg.setIcon(imageIcon);
                
            }
    }//GEN-LAST:event_btnUpdateStdActionPerformed

