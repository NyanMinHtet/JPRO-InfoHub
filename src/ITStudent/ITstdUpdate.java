     public int getStudentID() throws SQLException{
            int id=0;
            String maxId = "SELECT MAX(std_id) FROM student";
            
            PreparedStatement statement = con.prepareStatement(maxId);
            ResultSet rs = statement.executeQuery();
            
            
            while (rs.next()) {
                id = rs.getInt(1);
                
            }
             return id;
        }
     
      public void loadAllDataintoTableRS(ArrayList<Relationship>list){
        
        DefaultTableModel dtm=(DefaultTableModel) jTableRsA.getModel(); 
        dtm.setRowCount(0);
        
        for(Relationship rs :list){
            Vector v=new Vector();
            v.add(rs.getRid());
            v.add(rs.getName());
            v.add(rs.getPhno());
            v.add(rs.getOccupation());
            v.add(rs.getRs_type());

            dtm.addRow(v);
            
        } 
      }
      
            public void loadAllDataintoTableQua(ArrayList<Qualification>list){
        
        DefaultTableModel dtm=(DefaultTableModel) jTableEDUU3.getModel(); 
        dtm.setRowCount(0);
        
        for(Qualification qua :list){
            Vector v=new Vector();
           v.add(qua.getDip_name());
           v.add(qua.getField_name());
           v.add(qua.getType());
            v.add(qua.getOrg_name());
           v.add(qua.getFrom_date());
           v.add(qua.getTo_date());

            dtm.addRow(v);
            
        } 
      }
