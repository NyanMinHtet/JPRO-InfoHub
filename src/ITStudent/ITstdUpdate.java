             public void displaySelectedImage(File selectedFile) {
        try {
            Image image = ImageIO.read(selectedFile);
            if (image != null) {
                // Resize the image to fit the JLabel
                Image resizedImage = image.getScaledInstance(lblupdateimg.getWidth(),
                        lblupdateimg.getHeight(), Image.SCALE_SMOOTH);
              lblupdateimg.setIcon(new ImageIcon(resizedImage));
            } else {
                lblupdateimg.setIcon(null);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

