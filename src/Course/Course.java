          public void loadAllCourseDataintoTable(ArrayList<Course_Class>list){
        
        DefaultTableModel dtm=(DefaultTableModel) jTablecourse.getModel();
        dtm.setRowCount(0);
        
        for(Course_Class cc :list){
            Vector v=new Vector();
            v.add(cc.getCid());
            v.add(cc.getCno());
            v.add(cc.getCname());
            v.add(cc.getDu());
            v.add(cc.getFees());
            v.add(cc.getCdesc());
            dtm.addRow(v);
            
        } 
      }
  
        
        
     
