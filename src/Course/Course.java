    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        txtupcno.setVisible(true);
        int index = jTablecourse.getSelectedRow();
        if(index<0){
               JOptionPane.showMessageDialog(null, "Please select any record from table for update");
            }else
            {  
            try {
                boolean ch=inputUpdateCheck();
                if(ch==true){
                    DefaultTableModel model = (DefaultTableModel)jTablecourse.getModel();
                    
                    id = Integer.parseInt(jTablecourse.getValueAt(index, 0).toString());
                    
                    String no=txtupcno.getText();
                    String name=txtcName.getText();
                    String cdesc=txtcDesc.getText();
                    String cdu=txtcDu.getText();
                    int cfees = Integer.parseInt(txtcFees.getText());
                    Course_Class cc=new Course_Class(id,no,name,cdesc,cdu,cfees);
                    CourseDAO cdao=new CourseDAO();
                    int choice=JOptionPane.showConfirmDialog(null, "Do you really want to save your data","Confirm Message",JOptionPane.INFORMATION_MESSAGE);
                    if(choice==JOptionPane.YES_OPTION)
                        cdao.updateCourse_Data(cc);
                    loadcoursetable();
                    txtupcno.setText("");
                    txtcName.setText("");
                    txtcDu.setText("");
                    txtcFees.setText(String.valueOf(""));
                    txtcDesc.setText("");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Course.class.getName()).log(Level.SEVERE, null, ex);
            }
            }
    }//GEN-LAST:event_btnUpdateActionPerformed

